        valid = [(xi, yi) for xi, yi in zip(relaunch_x, relaunch_y) if yi is not None]
        if not valid or x < valid[0][0]:
            return 0.0
        for i in range(len(valid) - 1):
            x0, y0 = valid[i]
            x1, y1 = valid[i + 1]
            if x0 <= x <= x1:
                return y0 + (y1 - y0) * (x - x0) / (x1 - x0)
        return float(valid[-1][1])

