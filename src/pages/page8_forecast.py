@st.cache_data
def load_predictions():
    if not PRED_PATH.exists():
        return None
    with open(PRED_PATH) as f:
        return json.load(f)


def compute_trajectory(issue2_orders, retention):
    """
    Compute predicted trajectory + 95% CI for a given issue-2 baseline.
    Returns (issues, pred, ci_lo, ci_hi) as parallel lists.
    """
    issues = [2, 6, 12, 24, 36]
    pred, ci_lo, ci_hi = [issue2_orders], [issue2_orders], [issue2_orders]

    for pt in [6, 12, 24, 36]:
        key = str(pt)
        if key not in retention:
            pred.append(None); ci_lo.append(None); ci_hi.append(None)
            continue
        r    = retention[key]
        mean, std, n = r["mean"], r["std"], r["n"]
        p = issue2_orders * mean
        if n > 1:
            se    = std / np.sqrt(n)
            t_val = scipy_stats.t.ppf(0.975, df=n - 1)
            lo, hi = max(0, issue2_orders * (mean - t_val * se)), issue2_orders * (mean + t_val * se)
        else:
            lo, hi = p * 0.85, p * 1.15
        pred.append(p); ci_lo.append(lo); ci_hi.append(hi)

    return issues, pred, ci_lo, ci_hi


def compute_run_economics(pred, cover_price, run_length=24):
    """
    Estimate total orders and publisher gross revenue over a run using
    linear interpolation between checkpoint values.

    Arc definitions (non-overlapping, cover issues 2 through run_length):
      Launch:    issues  2-6   (5 issues)
      Early run: issues  7-12  (6 issues)
      Core run:  issues 13-24  (12 issues)  [24-issue total = 23 issues]
      Extended:  issues 25-36  (12 issues)  [36-issue total = 35 issues]
    """
    pub_net = cover_price * PUBLISHER_MARGIN

    # pred index: 0=issue2, 1=issue6, 2=issue12, 3=issue24, 4=issue36
    p2, p6, p12, p24 = pred[0], pred[1], pred[2], pred[3]
    p36 = pred[4] if len(pred) > 4 else None

    arcs = []
    if p2 and p6:
        arcs.append({"label": "Launch (#2-6)",     "n": 5,  "y0": p2,  "y1": p6})
    if p6 and p12:
        arcs.append({"label": "Early Run (#7-12)", "n": 6,  "y0": p6,  "y1": p12})
    if p12 and p24 and run_length >= 24:
        arcs.append({"label": "Core Run (#13-24)", "n": 12, "y0": p12, "y1": p24})
    if p24 and p36 and run_length >= 36:
        arcs.append({"label": "Extended (#25-36)", "n": 12, "y0": p24, "y1": p36})

    for arc in arcs:
        arc["avg_orders"] = (arc["y0"] + arc["y1"]) / 2
        arc["total_orders"] = arc["n"] * arc["avg_orders"]
        arc["revenue"] = arc["total_orders"] * pub_net

    total_issues = sum(a["n"] for a in arcs)
    total_orders = sum(a["total_orders"] for a in arcs)
    total_revenue = sum(a["revenue"] for a in arcs)

    # "Flat" baseline: same issue count at constant issue-2 readership (upper bound)
    flat_orders  = (p2 or 0) * total_issues
    flat_revenue = flat_orders * pub_net
    decay_cost   = flat_revenue - total_revenue

    return {
        "arcs":          arcs,
        "total_issues":  total_issues,
        "total_orders":  total_orders,
        "total_revenue": total_revenue,
        "flat_revenue":  flat_revenue,
        "decay_cost":    decay_cost,
        "pub_net":       pub_net,
    }


